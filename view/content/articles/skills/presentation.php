    <h2>PRÉSENTATION</h2>
    <p>Je me présenterais ici non brièvement. Je vous laisserai vous diriger vers l'onglet CV si 
        vous souhaitez en connaître davantage sur mon parcours professionnel.<br>
        <br>Pour vous donner une idée de qui je suis, pourquoi, ne pas vous parler en premier, de ce que j’aime ?<br> 
        Tout d'abord, puisque le sujet d'ici s'y prête (au moins un petit peu), j'ai toujours été intéressé par 
        les sciences.<br> 
        Pour moi, la science n'est autre que l'art d'expliquer le fonctionnement des choses. Elle permet de 
        comprendre ce qui se passe derrière ces choses, en dedans, avant et après. 
        On conviendra, que tout sujet peut être étudié. C'est pourquoi je m'autoriserais à dire que je suis
        passionné, aussi, par bien d'autres choses.<br>
        La création, Par exemple. C'est dans mes expériences passées, ce qui me procure le plus de plaisir.
        La sensation de partir de rien (ou presque), et de transformer ce "rien" en quelque chose de visible,
        palpable, utile et beau (au moins par son concept de naissance).<br>

        <br>Je vais maintenant faire un petit état des lieux de mes expériences, afin recentrer le sujet sur du concret.
        J’ai commencé assez rapidement à travailler. Mon premier emploi fut en tant qu'animateur de colonie de 
        vacances, à l’âge de 16 ans. Cet emploi se déroulait durant les vacances scolaires. J’ai obtenu par la 
        suite un baccalauréat économique et social en 2011. Celui-ci fut suivi par un voyage de 6 mois en Irlande et en 
        Angleterre. <b>(C’est durant cette période, que j'ai pu le plus rapidement perfectionner mon anglais)</b>
        <br>Je suis par la suite rentré en France, afin d'y poursuivre mes études.<br> 

        <br>La curiosité qui est mienne, dont je suis fière aujourd'hui, m'a joué des tours jadis. Effectivement, un 
        épisode de deux ans à l'université en Lettres et Arts <b>(C'est à cette période, que j’ai intégré le fait que 
        l’esthétique n’était jamais inutile)</b> ne se serra pas conclu en diplôme de licence, comme il l’aurait dû,
        mais en un CAP menuisier, passé avec Les compagnons du devoir. <b> (C'est ici, que j’ai appris la véritable rigueur)</b>
        <br>
        <br>C'est avec l'envie d'entreprendre que je pars ensuite en voyage. Direction l'Asie, cette fois. <b>(C’est comme ça, que j’ai appris beaucoup de choses)</b>
        Durant six mois, j'y apprendrai à travailler le bambou, dans six pays différents. (J'espère ne pas perdre votre estime à travers ces lignes qui peuvent paraître décousues.
        Ces écarts m'ont fait grandir. C'est grâce à eux que je sais aujourd'hui rebondir et m'adapter.) 
        <br>
        <br>De retour en France, je monte une entreprise de menuiserie qui durera cinq ans, et qui m'apportera bien des 
        connaissances <b>(C'est maintenant, que je deviens responsable)</b>, dans le domaine du bois, 
        mais également des compétences bien plus générales.<br>
        Parmi ces compétences, je citerais simplement l’autonomie, l’organisation, le dévouement, 
        la gestion humaine, la gestion de la clientèle, la gestion du stress, le plaisir au 
        travail… 
        <br>Mais également beaucoup d'autre choses, comme, la confiance en moi, la créativité et la réactivité... <br>
        <br>Aujourd’hui, c’est vers l’informatique que je me tourne. Je me suis orienté dans ce domaine presque par 
        hasard et j’en suis, à mon étonnement (pas si grand), tombé amoureux. Avec l’informatique, j’ai la sensation d’un 
        accomplissement, de toutes les étapes qui ont précédées celle-ci. Ce domaine est un monde, qui regroupe
        tout, et où tout est possible. Je crois, que c’est un monde, qui peut venir en aide à notre monde.
    </p>
    
